package lession_2;

public class OperatorsLearning {
    public static void main(String[] args) {
        int myNum1 = 1;
        int myNum2 = 2;

        int total = myNum1 + myNum2;
        System.out.println("Total: " + total);
        System.out.printf("Total is %d",total);
        System.out.printf("Float is %f\n",3.14);
        System.out.printf("Boolean value is %b\n",true);
        System.out.printf("Character value is %s\n",'a');

        int nguyen=5/3;
        int d=5%3;
        System.out.printf("5/3 is %d\n",nguyen);
        System.out.printf("d is %d\n",d);

        float myFloatNum=5/3f;
        System.out.println(myFloatNum);
    }
}
